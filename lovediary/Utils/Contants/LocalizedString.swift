extension String {
    init(localizedKey key: String) {
        var bundle: Bundle? = Bundle.main
        let availableLanguages = Bundle.preferredLocalizations(from: Language.allCases.map { $0.rawValue }).first
        if let path = Bundle.main.path(forResource: availableLanguages, ofType: "lproj") {
            bundle = Bundle(path: path)
        }
        self = bundle?.localizedString(forKey: key, value: nil, table: nil) ?? key
    }
}
